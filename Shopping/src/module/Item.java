package module;

public class Item {
    private int id;
    private String name;
    private double price;
    private float discount;
    private int quantity;

    public String getName() {
	return name;
    }

    public double getPrice() {
	return price;
    }

    public float getDiscount() {
	return discount;
    }

    public int getQuantity() {
	return quantity;
    }

}
